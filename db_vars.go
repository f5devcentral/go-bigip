type DBVariable struct {
	Name  string `json:"name,omitempty"`
	Value string `json:"value,omitempty"`
}

// GetDBVariable returns a named DB variable.
// Returns the error from the API call if any error occurs.
// This allows callers to properly handle API unavailability (502/503) vs not found.
func (b *BigIP) GetDBVariable(name string) (*DBVariable, error) {
	var dbVar DBVariable
	err, _ := b.getForEntity(&dbVar, uriSys, uriDb, name)
	if err != nil {
		return nil, err
	}

	return &dbVar, nil
}

// CreateDBVariable doesn't really create new configuration element
// Function will update old value with the existing value
func (b *BigIP) CreateDBVariable(config *DBVariable) error {
	return b.patch(config, uriSys, uriDb, config.Name)
}

// DeleteDBVariable resets a DB variable to its default value.
// DELETE method is not supported for database variables - this function
// instead updates the value to the default value from DefaultDBValues map.
func (b *BigIP) DeleteDBVariable(name string) error {
	val, ok := DefaultDBValues[name]
	if !ok {
		return fmt.Errorf("default value not found for DB variable '%s'. This variable may not be supported or may not have a default value defined", name)
	}

	stringVal, ok := val.(string)
	if !ok {
		return fmt.Errorf("default value for DB variable '%s' is not a string (got type %T)", name, val)
	}

	config := &DBVariable{
		Value: stringVal,
	}
	return b.patch(config, uriSys, uriDb, name)
}

// ModifyDBVariable allows the change of DB variable value
func (b *BigIP) ModifyDBVariable(config *DBVariable) error {
	return b.patch(config, uriSys, uriDb, config.Name)
}
